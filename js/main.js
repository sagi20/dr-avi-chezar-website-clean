// --- Standalone Carousel Logic (Works for both Dynamic and Static pages) ---
function initCarouselLogic() {
    const carouselContainer = document.getElementById('instaCarousel');
    const dotsContainer = document.getElementById('slideDots');
    const prevBtn = document.getElementById('prevSlide');
    const nextBtn = document.getElementById('nextSlide');

    if (!carouselContainer || !dotsContainer || !prevBtn || !nextBtn) return;

    const slides = carouselContainer.querySelectorAll('.insta-slide');
    const dots = dotsContainer.querySelectorAll('.insta-dot');

    if (slides.length === 0) return;

    let currentSlide = 0;

    // Find currently active slide if any (for static pages)
    slides.forEach((slide, index) => {
        if (slide.classList.contains('active')) {
            currentSlide = index;
        }
    });

    function showSlide(n) {
        slides[currentSlide].classList.remove('active');
        if (dots[currentSlide]) dots[currentSlide].classList.remove('active');

        currentSlide = (n + slides.length) % slides.length;

        slides[currentSlide].classList.add('active');
        if (dots[currentSlide]) dots[currentSlide].classList.add('active');
    }

    // --- Auto-play Logic ---
    let autoPlayInterval = setInterval(() => {
        showSlide(currentSlide + 1);
    }, 5000);

    function stopAutoPlay() {
        if (autoPlayInterval) {
            clearInterval(autoPlayInterval);
            autoPlayInterval = null;

            // Update button UI if it exists
            const pauseBtn = document.getElementById('pausePlayBtn');
            if (pauseBtn) {
                pauseBtn.innerHTML = '<i data-lucide="play"></i>';
                pauseBtn.title = "הפעל הפעלה אוטומטית";
                if (typeof lucide !== 'undefined') lucide.createIcons();
                pauseBtn.classList.add('paused');
            }
        }
    }

    // Handle Pause/Play button
    const pauseBtn = document.getElementById('pausePlayBtn');
    if (pauseBtn) {
        pauseBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            if (autoPlayInterval) {
                stopAutoPlay();
            } else {
                // Resume
                autoPlayInterval = setInterval(() => {
                    showSlide(currentSlide + 1);
                }, 5000);
                pauseBtn.innerHTML = '<i data-lucide="pause"></i>';
                pauseBtn.title = "עצור הפעלה אוטומטית";
                if (typeof lucide !== 'undefined') lucide.createIcons();
                pauseBtn.classList.remove('paused');
            }
        });
    }

    // Remove existing listeners to avoid duplicates (cloning trick)
    const newPrev = prevBtn.cloneNode(true);
    const newNext = nextBtn.cloneNode(true);
    prevBtn.parentNode.replaceChild(newPrev, prevBtn);
    nextBtn.parentNode.replaceChild(newNext, nextBtn);

    // Re-select after replacement
    const finalPrev = document.getElementById('prevSlide');
    const finalNext = document.getElementById('nextSlide');

    finalPrev.addEventListener('click', (e) => {
        e.stopPropagation();
        stopAutoPlay();
        showSlide(currentSlide - 1);
    });

    finalNext.addEventListener('click', (e) => {
        e.stopPropagation();
        stopAutoPlay();
        showSlide(currentSlide + 1);
    });

    // Also stop on dot clicks
    if (dotsContainer) {
        dotsContainer.addEventListener('click', stopAutoPlay, { once: true });
    }
}
