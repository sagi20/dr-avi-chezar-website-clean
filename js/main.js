document.addEventListener('DOMContentLoaded', () => {
    // Mobile Menu Toggle
    const mobileToggle = document.querySelector('.mobile-toggle');
    const navMenu = document.querySelector('.nav-menu');

    if (mobileToggle) {
        mobileToggle.addEventListener('click', () => {
            navMenu.classList.toggle('active');
            const icon = navMenu.classList.contains('active') ? '✕' : '☰';
            mobileToggle.textContent = icon;
        });
    }

    // Mobile Dropdown Toggle (Robust Version)
    const dropdownTriggers = document.querySelectorAll('.dropdown-trigger');

    dropdownTriggers.forEach(trigger => {
        trigger.addEventListener('click', (e) => {
            // Only prevent default if we are in mobile view (nav-menu has active class or screen is small)
            if (window.innerWidth <= 768) {
                e.preventDefault();
                e.stopPropagation(); // Prevent event bubbling

                const parent = trigger.closest('.nav-item-dropdown');
                const wasActive = parent.classList.contains('active');

                // Close all dropdowns first
                document.querySelectorAll('.nav-item-dropdown').forEach(item => {
                    item.classList.remove('active');
                });

                // If it wasn't active before, open it now
                if (!wasActive) {
                    parent.classList.add('active');
                }
            }
        });
    });

    // Close dropdowns when clicking outside
    document.addEventListener('click', (e) => {
        if (window.innerWidth <= 768) {
            if (!e.target.closest('.nav-item-dropdown')) {
                document.querySelectorAll('.nav-item-dropdown').forEach(item => {
                    item.classList.remove('active');
                });
            }
        }
    });

    // Header Scroll Effect
    const header = document.querySelector('.header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    // Smooth Scroll for Anchor Links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth'
                });
                // Close mobile menu if open
                navMenu.classList.remove('active');
                if (mobileToggle) mobileToggle.textContent = '☰';
            }
        });
    });





    // Scroll Animations (Intersection Observer)
    const observerOptions = {
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px"
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
                observer.unobserve(entry.target); // Only animate once
            }
        });
    }, observerOptions);

    document.querySelectorAll('.reveal').forEach(el => {
        observer.observe(el);
    });

    // FAQ Accordion
    const faqItems = document.querySelectorAll('.faq-item');
    faqItems.forEach(item => {
        const question = item.querySelector('.faq-question');
        if (question) {
            question.addEventListener('click', () => {
                const isActive = item.classList.contains('active');

                // Close all others
                faqItems.forEach(otherItem => {
                    otherItem.classList.remove('active');
                    const answer = otherItem.querySelector('.faq-answer');
                    if (answer) answer.style.maxHeight = null;
                });

                // Toggle current
                if (!isActive) {
                    item.classList.add('active');
                    const answer = item.querySelector('.faq-answer');
                    answer.style.maxHeight = answer.scrollHeight + "px";
                }
            });
        }
    });

    // --- Carousel Modal Logic ---
    const modal = document.getElementById('imageModal');
    if (modal) {
        const modalImg = document.getElementById('modalImage') || modal.querySelector('.modal-image');
        const modalCaption = document.getElementById('modalCaption') || modal.querySelector('.modal-caption');
        const modalCounter = document.getElementById('modalCounter'); // Optional
        const closeBtn = document.querySelector('.modal-close');

        // Robust selection: ID or Class
        const prevBtn = document.getElementById('modalPrev') || modal.querySelector('.modal-prev');
        const nextBtn = document.getElementById('modalNext') || modal.querySelector('.modal-next');

        let currentSlides = [];
        let currentSlideIndex = 0;

        function updateModalImage() {
            if (currentSlides.length > 0) {
                if (modalImg) modalImg.src = currentSlides[currentSlideIndex];

                // Update counter if exists and more than 1 slide
                if (currentSlides.length > 1) {
                    if (modalCounter) modalCounter.textContent = `${currentSlideIndex + 1} / ${currentSlides.length}`;
                    if (prevBtn) prevBtn.style.display = 'block';
                    if (nextBtn) nextBtn.style.display = 'block';
                } else {
                    if (modalCounter) modalCounter.textContent = '';
                    if (prevBtn) prevBtn.style.display = 'none';
                    if (nextBtn) nextBtn.style.display = 'none';
                }
            }
        }

        function openModalWithSlides(slides, caption) {
            currentSlides = slides;
            currentSlideIndex = 0;
            if (modalCaption) modalCaption.textContent = caption;
            updateModalImage();
            modal.classList.add('active');
            modal.style.display = 'flex';
            document.body.style.overflow = 'hidden';
        }

        function closeModal() {
            modal.classList.remove('active');
            modal.style.display = 'none';
            document.body.style.overflow = '';
            // Reset state
            currentSlides = [];
            currentSlideIndex = 0;
            if (modalImg) modalImg.src = '';
            if (modalCaption) modalCaption.textContent = '';
            if (modalCounter) modalCounter.textContent = '';
        }

        // Event Listeners for Nav
        // In Hebrew/RTL: Left button (prev) = forward, Right button (next) = backward
        if (prevBtn) {
            prevBtn.addEventListener('click', (e) => {
                e.stopPropagation();
                // Left arrow - go FORWARD (next slide)
                if (currentSlideIndex < currentSlides.length - 1) {
                    currentSlideIndex++;
                    updateModalImage();
                } else {
                    // Loop to start
                    currentSlideIndex = 0;
                    updateModalImage();
                }
            });
        }

        if (nextBtn) {
            nextBtn.addEventListener('click', (e) => {
                e.stopPropagation();
                // Right arrow - go BACKWARD (previous slide)
                if (currentSlideIndex > 0) {
                    currentSlideIndex--;
                    updateModalImage();
                } else {
                    // Loop to end
                    currentSlideIndex = currentSlides.length - 1;
                    updateModalImage();
                }
            });
        }

        // Attach listeners to items
        function attachModalListeners() {
            const feedItems = document.querySelectorAll('.feed-item');
            feedItems.forEach(item => {
                // Remove existing listener by using a named function
                item.removeEventListener('click', handleFeedItemClick);
                item.addEventListener('click', handleFeedItemClick);
            });
        }

        function handleFeedItemClick(e) {
            if (e.target.tagName === 'A') return;

            const img = this.querySelector('img');
            const caption = this.querySelector('h4').textContent;

            // Check for data-slides
            const slidesAttr = this.getAttribute('data-slides');
            let slides = [];

            if (slidesAttr) {
                slides = slidesAttr.split(',');
            } else {
                slides = [img.src];
            }

            openModalWithSlides(slides, caption);
        }

        // Initial attach
        attachModalListeners();

        closeBtn.addEventListener('click', closeModal);
        modal.addEventListener('click', (e) => {
            if (e.target === modal) closeModal();
        });
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape' && modal.classList.contains('active')) closeModal();
            if (modal.classList.contains('active')) {
                if (e.key === 'ArrowLeft') prevBtn.click();
                if (e.key === 'ArrowRight') nextBtn.click();
            }
        });

        // --- Dynamic Feed Shuffle (Updated) ---
        const shuffleBtn = document.getElementById('shuffleFeedBtn');

        // Generate postsPool dynamically from medicalContent
        // This ensures all topics are available in the feed
        const postsPool = Object.values(medicalContent).filter(item => item.title !== 'מידע רפואי' && item.slides && item.slides.length > 0).map(item => ({
            image: item.mediaImage, // Cover image
            title: item.title,
            desc: item.subtitle,
            slides: item.slides || [] // Full array of slides, default to empty array
        }));

        // Initialize Content Center Grid (if on content-center.html)
        // This runs independently, not tied to shuffleBtn
        if (window.location.pathname.includes('content-center.html')) {
            const contentGrid = document.getElementById('feedGrid');
            if (contentGrid && postsPool.length > 0) {
                contentGrid.innerHTML = postsPool.map((post, index) => {
                    const slideUrls = post.slides.join(',');
                    return `
                    <div class="feed-item reveal active" data-slides="${slideUrls}">
                        <img src="${post.image}" alt="${post.title}">
                        <div class="feed-overlay">
                            <h4>${post.title}</h4>
                            <p>${post.desc}</p>
                            <span style="font-size:0.8rem; display:block; margin-top:0.5rem;">
                                <i data-lucide="layers"></i> ${post.slides.length} שקופיות
                            </span>
                        </div>
                    </div>
                `}).join('');

                attachModalListeners();
                lucide.createIcons();
            }
        }

        if (shuffleBtn) {
            const feedGrid = document.getElementById('feedGrid');

            // Function to render random posts
            const renderRandomPosts = () => {
                console.log('Rendering random posts. Pool size:', postsPool.length);
                if (postsPool.length === 0) {
                    console.error('Posts pool is empty!');
                    return;
                }
                const shuffled = postsPool.sort(() => 0.5 - Math.random()).slice(0, 3);

                feedGrid.innerHTML = shuffled.map((post, index) => {
                    // Generate slide URLs string for data attribute
                    const slideUrls = post.slides.join(',');

                    return `
                    <div class="feed-item reveal active" data-slides="${slideUrls}">
                        <img src="${post.image}" alt="${post.title}">
                        <div class="feed-overlay">
                            <h4>${post.title}</h4>
                            <p>${post.desc}</p>
                            <span style="font-size:0.8rem; display:block; margin-top:0.5rem;">
                                <i data-lucide="layers"></i> ${post.slides.length} שקופיות
                            </span>
                        </div>
                    </div>
                `}).join('');

                // Re-attach listeners
                attachModalListeners();
                lucide.createIcons(); // Refresh icons for new items
            };

            // Initial Render (Run immediately to replace placeholders)
            renderRandomPosts();

            shuffleBtn.addEventListener('click', () => {
                console.log('Shuffle button clicked');
                const icon = shuffleBtn.querySelector('i');
                if (icon) icon.classList.add('spin-animation');

                feedGrid.style.opacity = '0';
                feedGrid.style.transform = 'translateY(10px)';

                setTimeout(() => {
                    renderRandomPosts();
                    feedGrid.style.opacity = '1';
                    feedGrid.style.transform = 'translateY(0)';

                    setTimeout(() => {
                        if (icon) icon.classList.remove('spin-animation');
                    }, 500);
                }, 300);
            });
        }


    }
});

